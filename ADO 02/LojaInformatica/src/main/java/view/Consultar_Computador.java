    public void Tabela() {

        try {
            ArrayList<Computador> listaPc = sql.listarNoBanco();
            DefaultTableModel table = (DefaultTableModel) tblComputadores.getModel();
            table.setRowCount(0);
            for (Computador lista : listaPc) {
                table.addRow(new Object[]{
                    lista.getId(),
                    lista.getProcessador(),
                    lista.getMarca(),
                    lista.getHd()
                });
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao listar os computadores:\n" + ex.getMessage(), "Erro", JOptionPane.ERROR_MESSAGE);
        }

    }

