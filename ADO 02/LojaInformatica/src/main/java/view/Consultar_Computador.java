    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if (txtFiltro.getText().trim().equals("")) {
            this.Tabela();
        } else {
            String pr = txtFiltro.getText();
            ArrayList<Computador> list = sql.buscarProcessador(pr);

            DefaultTableModel table = (DefaultTableModel) tblComputadores.getModel();
            table.setRowCount(0);

            if (!list.isEmpty()) {
                for (Computador lista : list) {
                    table.addRow(new Object[]{
                        lista.getId(),
                        lista.getProcessador(),
                        lista.getMarca(),
                        lista.getHd()
                    });
                }
            } else {
                JOptionPane.showMessageDialog(this, "Nenhum resultado encontrado.");
            }
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed

        int lS = tblComputadores.getSelectedRow();

        if (lS == -1) {
            JOptionPane.showMessageDialog(null, "Escolha uma linha para alterar!");
            return;
        }

        DefaultTableModel tabela = (DefaultTableModel) tblComputadores.getModel();

        Object idObject = tabela.getValueAt(lS, 0);
        Object proObject = tabela.getValueAt(lS, 1);
        Object hdObject = tabela.getValueAt(lS, 3);

        int id = Integer.parseInt(idObject.toString());
        String pro = proObject.toString();
        int hd = Integer.parseInt(hdObject.toString());

        Computador PC = new Computador(hd, pro, id);

        Cad_computador tela = new Cad_computador(PC);
        tela.setVisible(true);
        tela.setLocationRelativeTo(null);
    }//GEN-LAST:event_btnAlterarActionPerformed

