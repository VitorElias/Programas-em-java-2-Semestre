    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int lS = tblComputadores.getSelectedRow();

        if (lS == -1) {
            JOptionPane.showMessageDialog(null, "NENHUMA LINHA SELECIONADA!!!");
            return;
        }

        DefaultTableModel tabela = (DefaultTableModel) tblComputadores.getModel();
        int id = Integer.parseInt(tabela.getValueAt(lS, 0).toString());

        boolean re = sql.excluirNoBanco(id);

        if (re) {
            JOptionPane.showMessageDialog(null, "Excluido!");
        } else {
            JOptionPane.showMessageDialog(null, "Houve um erro ao excluir!");
        }

        this.Tabela();

    }//GEN-LAST:event_btnExcluirActionPerformed

