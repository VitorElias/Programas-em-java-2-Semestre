    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed

        String p = txtProcessador.getText();
        int hd = Integer.parseInt(txtHd.getText());

        if (pc == null) {
            Computador newPc = new Computador(p, hd);
            boolean r = sql.salvarNoBanco(newPc);

            if (r) {
                JOptionPane.showMessageDialog(null, "Cadastrado!");
            } else {
                JOptionPane.showMessageDialog(null, "NÃ£o foi cadastrado!!");
            }
        } else {
            pc.setProcessador(p);
            pc.setHd(hd);

            boolean r = sql.alterarNoBanco(pc);

            if (r) {
                JOptionPane.showMessageDialog(null, "Sucesso!");
            } else {
                JOptionPane.showMessageDialog(null, "ERRO");
            }

            dispose();
        }


    }//GEN-LAST:event_btnCadastrarActionPerformed

